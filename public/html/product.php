<script>
    var count = $(".slides>li").length;

    function initMax() {
        var max = parseInt(count / getGridSize());
        if (count % getGridSize() > 0) {
            max += 1;
        }
        $("#max-slide").text(max);
    }

    // tiny helper function to add breakpoints
    function getGridSize() {
        var w = $(".flexslider").outerWidth();
        if (w < 560) {
            return 2;
        }
        if (w >= 560 && w < 720) {
            return 4
        }
        if (w >= 720 && w < 1000) {
            return 6;
        }
        return 8;
    }

    $(function () {
        SyntaxHighlighter.all();
    });

    var slider = $('.flexslider').flexslider({
        animation: "slide",
        animationLoop: false,
        slideshow: false,
        controlNav: false,
        directionNav: false,
        touch: false,
        rtl: true,
        mousewheel: false,
        itemWidth: 210,
        itemMargin: 15,
        minItems: getGridSize(), // use function to pull in initial value
        maxItems: getGridSize(), // use function to pull in initial value
        init: function () {
            initMax();
        },
        after: function (slide) {
            $("#current-slide").text(slide.currentSlide + 1);
        }
    });

    $('.toggle-next').on('click', function () {
        slider.flexslider('next');
    })

    $('.toggle-prev').on('click', function () {
        slider.flexslider('prev');
    })

    // check grid size on resize event
    $(window).resize(function () {
        initMax();
        slider.flexslider({
            minItems: getGridSize(),
            maxItems: getGridSize(),
        });
        slider.flexslider(0);
    });
</script>
